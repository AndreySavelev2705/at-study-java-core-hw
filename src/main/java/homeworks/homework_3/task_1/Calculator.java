package homeworks.homework_3.task_1;

public class Calculator {

    public int sum(int nArg1, int nArg2) {

        return nArg1 + nArg2;
    }

    public int subtract(int nArg1, int nArg2) {

        return nArg1 - nArg2;
    }

    public int multiply(int nArg1, int nArg2) {

        return nArg1 * nArg2;
    }

    public int divide(int nArg1, int nArg2) {

        return nArg1 / nArg2;
    }
}
